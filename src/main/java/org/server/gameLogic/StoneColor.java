package org.server.gameLogic;

/**
 * The StoneColor enum represents the possible colors of stones on a Go game board.
 * It includes BLACK, WHITE, N empty, and REMOVED (indicating a removed stone).
 */
public enum StoneColor {
    /**
     * Black stone color.
     */
    BLACK,

    /**
     * White stone color.
     */
    WHITE,

    /**
     * Empty stone color.
     */
    N,

    /**
     * Removed stone color, indicating a stone that has been removed from the board.
     */
    REMOVED
}

